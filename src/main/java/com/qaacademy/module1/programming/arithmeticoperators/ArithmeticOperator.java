package com.qaacademy.module1.programming.arithmeticoperators;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int x = 5;
        x = x + 4;
        System.out.println(x); // 9

        int x1 = 5;
        x1 += 3;
        System.out.println(x1); // 8

        int x2 = 10;
        x2 = x2 - 3;
        System.out.println(x2); // 7

        int x3 = 10;
        x2 -= 4;
        System.out.println(x3); // 6

        int x4 = 4;
        x4 = x4 * 3;
        System.out.println(x4); // 12

        int x5 = 5;
        x5 *= 3;
        System.out.println(x5); // 15

        int x6 = 33;
        x6 = x6 % 5;
        System.out.println(x6); // 3

        int x7 = 63;
        x7 %= 2;
        System.out.println(x7); // 0
    }
}
